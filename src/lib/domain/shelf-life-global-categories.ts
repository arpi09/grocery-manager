export function guessDaysFromCategoryHint(
	categoryHint: string | null | undefined,
	location: StorageLocation
): number | null {
	if (!categoryHint) return null;
	const key = categoryHint.trim().toLowerCase();
	const base = CATEGORY_HINT_DAYS[key];
	if (base == null) return null;
	if (location === 'freezer') return base + 90;
	if (location === 'cupboard') return base + 30;
	return base;
}
